<p>
Final Fantasy VII (ファイナルファンタジーVII, Fainaru Fantajī Sebun?) est un jeu vidéo de rôle développé par Square (devenu depuis Square Enix) sous la direction de Yoshinori Kitase et sorti en 1997, constituant le septième opus de la série Final Fantasy. Premier jeu de la série à être produit pour la console Sony PlayStation ainsi que pour les ordinateurs dotés de Windows, c’est aussi le premier Final Fantasy à utiliser des graphismes en 3D avec des personnages rendus en temps réel et des arrière-plans précalculés.

L’histoire du jeu, dans une perspective eschatologique, est centrée sur le combat d’un groupe éco-terroriste nommé « Avalanche » contre une puissante organisation appelée la « Shinra », qui exploite jusqu'à épuisement l’énergie vitale de la planète pour produire de l'énergie et des armes. Au fur et à mesure du jeu, le conflit s’intensifie et l'avenir du monde devient l'enjeu majeur.

Énorme succès critique et commercial, le jeu s’est vendu à plus de 9,7 millions d’exemplaires à travers le monde3, ce qui contribua grandement à amorcer le succès de la PlayStation, ainsi que l’exportation des RPG du même type hors du Japon. La grande popularité du jeu pousse Square Enix à produire une série de suites et de préquelles à partir de 2004, réunies sous le titre collectif de Compilation of Final Fantasy VII.

Un remake du jeu sous forme de trilogie est en cours de publication par Square Enix. Le premier épisode, Final Fantasy VII Remake, comprend la partie du jeu original se déroulant à Midgar, est sortie sur PlayStation 4 en avril 2020, puis sur PlayStation 5 et Microsoft Windows en 2021. La deuxième partie, Final Fantasy VII Rebirth, est prévue pour le 29 février 2024, tandis que la troisième partie est également en cours de développement. 
</p>


